# here the variable string has the string "hello" assigned to it
tup = 'Hello'
# in the print command we ask it to count the length of string in tup
print(len(tup))
#OUTPUT: 5 this is because there are 5 characters in the string "Hello"

# this time the comma after the 'Hello' ensures the it is recognised as a tuple
tup = 'Hello',
# the len will return the number of elements in the tuple which is 1
print(len(tup))